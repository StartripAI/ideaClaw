class PdfExporter:
    """PDF exporter integrated with IdeaClaw pipeline.

    Tries WeasyPrint → ReportLab → HTML fallback in order.
    """

    def __init__(self, config: Optional[PdfConfig] = None):
        self.config = config or PdfConfig()
        self._engine = self._resolve_engine()

    def export(
        self,
        pack_content: str,
        output_path: Path,
        metadata: Optional[Dict[str, Any]] = None,
        pqs_scores: Optional[Dict[str, float]] = None,
    ) -> Path:
        """Export pack content as PDF.

        Args:
            pack_content: Markdown content.
            output_path: Path to write PDF.
            metadata: Optional dict with title, authors, abstract, date.
            pqs_scores: Optional quality scores for audit footer.

        Returns:
            Path to the generated PDF.
        """
        output_path.parent.mkdir(parents=True, exist_ok=True)
        metadata = metadata or {}

        logger.info("Exporting PDF via %s engine → %s", self._engine, output_path)

        if self._engine == "weasyprint":
            self._export_weasyprint(pack_content, output_path, metadata, pqs_scores)
        elif self._engine == "reportlab":
            self._export_reportlab(pack_content, output_path, metadata, pqs_scores)
        else:
            self._export_html_fallback(pack_content, output_path, metadata, pqs_scores)

        logger.info("PDF exported: %s (%d bytes)", output_path, output_path.stat().st_size)
        return output_path

    # ---- WeasyPrint engine ----

    def _export_weasyprint(
        self, content: str, output_path: Path,
        metadata: Dict[str, Any], pqs_scores: Optional[Dict[str, float]],
    ) -> None:
        html_str = self._markdown_to_html(content, metadata, pqs_scores)
        doc = weasyprint.HTML(string=html_str)
        doc.write_pdf(str(output_path))

    # ---- ReportLab engine ----

    def _export_reportlab(
        self, content: str, output_path: Path,
        metadata: Dict[str, Any], pqs_scores: Optional[Dict[str, float]],
    ) -> None:
        doc = SimpleDocTemplate(
            str(output_path), pagesize=A4,
            leftMargin=self.config.margin_mm * mm,
            rightMargin=self.config.margin_mm * mm,
            topMargin=self.config.margin_mm * mm,
            bottomMargin=self.config.margin_mm * mm,
        )
        styles = getSampleStyleSheet()
        story: List[Any] = []

        # Cover
        if self.config.include_cover and metadata.get("title"):
            story.append(Spacer(1, 80 * mm))
            story.append(Paragraph(
                f"<font size=24><b>{html.escape(metadata['title'])}</b></font>",
                styles["Title"],
            ))
            if metadata.get("authors"):
                authors = ", ".join(metadata["authors"]) if isinstance(metadata["authors"], list) else metadata["authors"]
                story.append(Paragraph(f"<font size=14>{html.escape(authors)}</font>", styles["Normal"]))
            story.append(Spacer(1, 10 * mm))
            if metadata.get("abstract"):
                story.append(Paragraph(
                    f"<i>{html.escape(metadata['abstract'][:500])}</i>",
                    styles["Normal"],
                ))
            story.append(PageBreak())

        # Content — split by lines and convert to paragraphs
        for line in content.split("\n"):
            stripped = line.strip()
            if not stripped:
                story.append(Spacer(1, 3 * mm))
            elif stripped.startswith("# "):
                story.append(Paragraph(
                    f"<font size=16><b>{html.escape(stripped[2:])}</b></font>",
                    styles["Heading1"],
                ))
            elif stripped.startswith("## "):
                story.append(Paragraph(
                    f"<font size=13><b>{html.escape(stripped[3:])}</b></font>",
                    styles["Heading2"],
                ))
            elif stripped.startswith("### "):
                story.append(Paragraph(
                    f"<font size=11><b>{html.escape(stripped[4:])}</b></font>",
                    styles["Heading3"],
                ))
            elif stripped.startswith("- ") or stripped.startswith("* "):
                story.append(Paragraph(f"• {html.escape(stripped[2:])}", styles["BodyText"]))
            else:
                story.append(Paragraph(html.escape(stripped), styles["BodyText"]))

        # PQS footer
        if pqs_scores:
            story.append(Spacer(1, 10 * mm))
            scores_str = " | ".join(f"{k}: {v:.2f}" for k, v in pqs_scores.items())
            story.append(Paragraph(
                f"<font size=8 color='gray'>Quality: {scores_str}</font>",
                styles["Normal"],
            ))

        doc.build(story)

    # ---- HTML fallback ----

    def _export_html_fallback(
        self, content: str, output_path: Path,
        metadata: Dict[str, Any], pqs_scores: Optional[Dict[str, float]],
    ) -> None:
        html_content = self._markdown_to_html(content, metadata, pqs_scores)
        html_path = output_path.with_suffix(".html")
        html_path.write_text(html_content, encoding="utf-8")
        logger.warning(
            "No PDF engine available. Wrote HTML fallback → %s. "
            "Install weasyprint or reportlab for PDF.", html_path,
        )

    # ---- Shared helpers ----

    def _markdown_to_html(
        self, content: str, metadata: Dict[str, Any],
        pqs_scores: Optional[Dict[str, float]],
    ) -> str:
        """Convert markdown to styled HTML for PDF rendering."""
        cfg = self.config
        theme = CSS_THEMES.get(cfg.css_theme, CSS_THEMES["academic"])
        css = theme % {
            "header": html.escape(cfg.header_text or metadata.get("title", "")),
            "footer": html.escape(cfg.footer_text),
        }

        parts = [f"<html><head><style>{css}</style></head><body>"]

        # Watermark
        if cfg.include_watermark:
            parts.append(f'<div class="watermark">{html.escape(cfg.watermark_text)}</div>')

        # Cover page
        if cfg.include_cover and metadata.get("title"):
            parts.append('<div class="cover">')
            parts.append(f'<h1>{html.escape(metadata["title"])}</h1>')
            if metadata.get("authors"):
                authors = metadata["authors"]
                if isinstance(authors, list):
                    authors = ", ".join(authors)
                parts.append(f'<div class="authors">{html.escape(authors)}</div>')
            if metadata.get("date"):
                parts.append(f'<div class="date">{html.escape(metadata["date"])}</div>')
            if metadata.get("abstract"):
                parts.append(f'<div class="abstract"><b>Abstract.</b> {html.escape(metadata["abstract"])}</div>')
            parts.append("</div>")

        # TOC
        if cfg.include_toc:
            toc = self._extract_toc(content)
            if toc:
                parts.append('<div class="toc"><h2>Contents</h2><ul>')
                for level, title in toc:
                    indent = (level - 1) * 20
                    parts.append(f'<li style="margin-left:{indent}px">{html.escape(title)}</li>')
                parts.append("</ul></div>")

        # Main content — basic markdown→html
        body = self._md_to_html_body(content)
        parts.append(body)

        # PQS footer
        if pqs_scores:
            parts.append('<div class="pqs-footer">')
            parts.append("<b>Quality Scores:</b> ")
            parts.append(" | ".join(f"{k}: {v:.2f}" for k, v in pqs_scores.items()))
            parts.append("</div>")

        parts.append("</body></html>")
        return "\n".join(parts)

    @staticmethod
    def _extract_toc(content: str) -> List[tuple]:
        """Extract table of contents from markdown headings."""
        toc = []
        for line in content.split("\n"):
            m = re.match(r"^(#{1,4})\s+(.+)$", line)
            if m:
                toc.append((len(m.group(1)), m.group(2).strip()))
        return toc

    @staticmethod
    def _md_to_html_body(content: str) -> str:
        """Basic markdown → HTML conversion (no external deps)."""
        lines = content.split("\n")
        html_lines = []
        in_code = False

        for line in lines:
            stripped = line.strip()

            if stripped.startswith("```"):
                if in_code:
                    html_lines.append("</pre>")
                    in_code = False
                else:
                    html_lines.append("<pre>")
                    in_code = True
                continue

            if in_code:
                html_lines.append(html.escape(line))
                continue

            if not stripped:
                html_lines.append("<br>")
            elif stripped.startswith("#### "):
                html_lines.append(f"<h4>{html.escape(stripped[5:])}</h4>")
            elif stripped.startswith("### "):
                html_lines.append(f"<h3>{html.escape(stripped[4:])}</h3>")
            elif stripped.startswith("## "):
                html_lines.append(f"<h2>{html.escape(stripped[3:])}</h2>")
            elif stripped.startswith("# "):
                html_lines.append(f"<h1>{html.escape(stripped[2:])}</h1>")
            elif stripped.startswith("> "):
                html_lines.append(f"<blockquote>{html.escape(stripped[2:])}</blockquote>")
            elif stripped.startswith("- ") or stripped.startswith("* "):
                html_lines.append(f"<li>{html.escape(stripped[2:])}</li>")
            elif stripped.startswith("---"):
                html_lines.append("<hr>")
            elif stripped.startswith("|") and "|" in stripped[1:]:
                cells = [c.strip() for c in stripped.split("|")[1:-1]]
                if all(set(c) <= {"-", ":"} for c in cells):
                    continue  # separator row
                tag = "th" if not html_lines or "<table>" == html_lines[-1] else "td"
                row = "".join(f"<{tag}>{html.escape(c)}</{tag}>" for c in cells)
                if tag == "th" and (not html_lines or html_lines[-1] != "<table>"):
                    html_lines.append("<table>")
                html_lines.append(f"<tr>{row}</tr>")
            else:
                # Inline formatting
                text = html.escape(stripped)
                text = re.sub(r"\*\*(.+?)\*\*", r"<b>\1</b>", text)
                text = re.sub(r"\*(.+?)\*", r"<i>\1</i>", text)
                text = re.sub(r"`(.+?)`", r"<code>\1</code>", text)
                html_lines.append(f"<p>{text}</p>")

        if in_code:
            html_lines.append("</pre>")

        return "\n".join(html_lines)

    def _resolve_engine(self) -> str:
        """Determine which engine to use."""
        pref = self.config.engine
        if pref == "auto":
            if HAS_WEASYPRINT:
                return "weasyprint"
            if HAS_REPORTLAB:
                return "reportlab"
            return "html_fallback"
        if pref == "weasyprint" and HAS_WEASYPRINT:
            return "weasyprint"
        if pref == "reportlab" and HAS_REPORTLAB:
            return "reportlab"
        return "html_fallback"

    @property
    def available_engines(self) -> List[str]:
        """List available PDF engines."""
        engines = ["html_fallback"]
        if HAS_WEASYPRINT:
            engines.insert(0, "weasyprint")
        if HAS_REPORTLAB:
            engines.insert(0, "reportlab")
        return engines
