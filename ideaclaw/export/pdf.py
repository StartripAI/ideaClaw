"""PDF export — dual-engine PDF generation integrated with the IdeaClaw pipeline.

Engines (priority order):
  1. WeasyPrint — CSS-based, high-fidelity HTML→PDF
  2. ReportLab — programmatic, no external deps

Integrates with:
  - export.Exporter → called as PdfExporter().export(content, path)
  - export.styles  → uses LaTeX style registry for academic formatting
  - quality.report → includes PQS scores in the audit footer
  - library.personalize → respects user style preferences

Usage:
    from ideaclaw.export.pdf import PdfExporter
    exporter = PdfExporter()
    exporter.export(markdown_content, Path("output.pdf"), metadata={...})
"""

from __future__ import annotations

import html
import logging
import re
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any, Dict, List, Optional

logger = logging.getLogger(__name__)

# Engine detection
try:
    import weasyprint  # type: ignore
    HAS_WEASYPRINT = True
except ImportError:
    HAS_WEASYPRINT = False

try:
    from reportlab.lib.pagesizes import A4  # type: ignore
    from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle  # type: ignore
    from reportlab.platypus import (  # type: ignore
        SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle,
        PageBreak, Image as RLImage,
    )
    from reportlab.lib.units import mm, cm  # type: ignore
    from reportlab.lib import colors  # type: ignore
    HAS_REPORTLAB = True
except ImportError:
    HAS_REPORTLAB = False


__all__ = ["PdfExporter", "PdfConfig"]


@dataclass
class PdfConfig:
    """PDF generation configuration."""
    engine: str = "auto"           # auto|weasyprint|reportlab|html_fallback
    page_size: str = "A4"
    margin_mm: float = 25.0
    font_family: str = "serif"
    font_size_pt: int = 11
    header_text: str = ""
    footer_text: str = "Generated by IdeaClaw"
    include_toc: bool = True
    include_cover: bool = True
    include_watermark: bool = False
    watermark_text: str = "DRAFT"
    css_theme: str = "academic"    # academic|minimal|modern


